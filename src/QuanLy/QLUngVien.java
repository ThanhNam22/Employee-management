    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        try {
                kt=1;
                //lấy dữ liệu tạo đối tượng
                uv = new UngVien(txtmaungvien.getText(), txttenungvien.getText(), 
                txtdiachi.getText(),layGioiTinh(), txtemail.getText(), txtsodt.getText(), txtngaysinh.getText());
                if (    uv.getMaUngVien().trim().compareTo("")==0
                        || uv.getNgaySinh().trim().compareTo("")==0
                        || uv.getHoTen().trim().compareTo("")==0
                        || uv.getDiaChi().trim().compareTo("")==0
                        || uv.getGioiTinh().trim().compareTo("")==0
                        || uv.getEmail().trim().compareTo("")==0
                        || uv.getSoDT().trim().compareTo("")==0){
                    throw new Exception("Không được để trống thông tin ");
                }
                if (Check(txtmaungvien.getText()))
                {
                    throw new Exception("Mã ứng viên không được trùng");
                }
        } catch (Exception ex) {
            kt=0;
            JOptionPane.showMessageDialog(this, 
                    ex.toString(),"Thông báo",WIDTH);
        }
        if (kt==1)
            dsUV.add(uv);
        loadTableUngVien();//load lại bảng với dl mới
    }//GEN-LAST:event_btnthemActionPerformed

