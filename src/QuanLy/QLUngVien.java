    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        //câp nhật dữ liệu mới tại vị trí dòng chọn
           dongChon=tbungvien.getSelectedRow();
           String x = (String) tbungvien.getValueAt(dongChon, 0);
           if (dongChon != -1) {
           UngVien uvNew = new UngVien();
           try {
                    kt=1;
                    uvNew.setMaUngVien(x+"");
                    uvNew.setHoTen(txttenungvien.getText()+"");
                    uvNew.setDiaChi(txtdiachi.getText()+"");
                    uvNew.setGioiTinh(layGioiTinh()+"");
                    uvNew.setEmail(txtemail.getText()+"");
                    uvNew.setSoDT(txtsodt.getText()+"");
                    uvNew.setNgaySinh(txtngaysinh.getText()+"");
                    if (//uvNew.getMaUngVien().trim().compareTo("")==0
                         uvNew.getNgaySinh().trim().compareTo("")==0
                        || uvNew.getHoTen().trim().compareTo("")==0
                        || uvNew.getDiaChi().trim().compareTo("")==0
                        || uvNew.getGioiTinh().trim().compareTo("")==0
                        || uvNew.getEmail().trim().compareTo("")==0
                        || uvNew.getSoDT().trim().compareTo("")==0
                      ){
                        throw new Exception("Không được để trống thông tin ");
                    }
                    if (x.trim().compareTo(txtmaungvien.getText().trim())!=0) {
                        throw new Exception("Không được sửa mã ứng viên");
                   }
               } catch (Exception ex) {
                   JOptionPane.showMessageDialog(this,
                    ex.toString(),"Thông báo", WIDTH);
                    kt=0;
               }
           
           
           if (kt==1)
                dsUV.set(dongChon, uvNew);//vị trí là dòng chọn
           //dữ liệu mới là từ giao diện cập nhật
           loadTableUngVien();
        }
        else
            JOptionPane.showMessageDialog(this,
                    "Chưa chọn dòng sửa","Thông báo", WIDTH);
    }//GEN-LAST:event_btnsuaActionPerformed

    private void tbungvienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbungvienMouseClicked
        dongChon = tbungvien.getSelectedRow();
        if (dongChon != -1) {
            uv = dsUV.get(dongChon);            
            txtmaungvien.setText(uv.getMaUngVien()+"");
            txttenungvien.setText(uv.getHoTen()+"");
            txtdiachi.setText(uv.getDiaChi()+"");
            if (uv.getGioiTinh().trim().compareTo("Nam")==0)
                rdonam.setSelected(true);
            else
                rdonu.setSelected(true);
            txtemail.setText(uv.getEmail()+"");
            txtsodt.setText(uv.getSoDT()+"");
            txtngaysinh.setText(uv.getNgaySinh()+"");
        }
    }//GEN-LAST:event_tbungvienMouseClicked

